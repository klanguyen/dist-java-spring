package edu.wctc.spring.beans;

public class FeaturedCrew {
    private int crewId;
    private String name;

    public FeaturedCrew() {
    }

    public int getCrewId() {
        return crewId;
    }

    public void setCrewId(int crewId) {
        this.crewId = crewId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
